		$date = intval($match[1]);
		$month = $match[2];
		$year = intval($match[3]);

		//Handle 2-digit year 2000
		if ($year<1000) {
			$year += 2000;
		}

		$hour = intval($match[4]);
		$min = intval($match[5]);
		$sec = intval($match[6]);
		$tz = null;
		if (isset($match[7])) $tz=$match[7];
		//Convert month code to month index
		$month = strtolower($month);
		$monthNum = $oz_w3c_Months[$month];
		//May be a number
		if (empty($monthNum)) {
			$monthNum = intval($month);
		}

		//Get timezone. Add/subtract
		if (!empty($tz)) {
			//PHP4 cannot handle years >= 2038
			if (!oz_is_php5()) {
				if (intval($year)>=2038) $year=2037;
			}
			//Workaround warning message
			if ($year<=1970) $year=1971;
			$time = gmmktime($hour,$min,$sec,$monthNum,$date,$year);
			$offset = w3c_getTimeZoneOffset($tz);
			if ($offset!=null) {
				$time -= intval($offset);
			}
		}
		else {
			//If no timezone info, use local time instead
			$time = mktime($hour,$min,$sec,$monthNum,$date,$year);
		}
		return $time;
	}
	else {
		return null;
	}
}

function oz_jsdecode ($js) {
	$sb = '';
	$n = strlen($js);
	$escapeChar = false;
	for ($i=0; $i<$n; $i++) {
		$c = $js[$i];
		if ($escapeChar) {
			switch ($c) {
			case 'r':
				$sb.="\r";
				$escapeChar = false;
				break;
			case 'n':
				$sb.="\n";
				$escapeChar = false;
				break;
			case 't':
				$sb.="\t";
				$escapeChar = false;
				break;
			case 'b':
				$sb.="\b";
				$escapeChar = false;
				break;
			case 'x':
				if ($i + 3 < $n)
				{
					$hex = substr($js,$i+1,2);
					$v = hexdec($hex);
					$sb.=chr($v);
					//$sb.=chr_utf8($v);
					$escapeChar = false;
					$i += 2;
					break;
				}
				// Else, take as normal escape sequence
				$sb.=$c;
				$escapeChar = false;
				break;

			case 'u':
				if ($i + 5 < $n)
				{
					$hex = substr($js,$i+1,4);
					$v = hexdec($hex);
					$sb.=chr_utf8($v);
					$escapeChar = false;
					$i += 4;
					break;
				}
				// Else, take as normal escape sequence
				$sb.=$c;
				$escapeChar = false;
				break;
			// TODO FUTURE SUPPORT FOR \\U, which is 32-bit unicode
			case '\\':
			case '"':
			case '\'':
			default:
				$sb.=$c;
				$escapeChar = false;
				break;
			}
		}
		else {
			if ($c == '\\')
			{
				$escapeChar = true;
			}
			else
			{
				$sb.=$c;
			}
		}
	}
	return $sb;
}

