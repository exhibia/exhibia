function utf16toutf8($v)
{
	$s = '';
	$n = strlen($v);
	for ($i=0; $i<$n; $i+=2) {
		$c1 = $v[$i];
		$c2 = $v[$i+1];
		//LE,BE
		$c = (ord($c2)<<8) | ord($c1);
//		echo "[$c1:$c2]";
		if ($c<0x80) {
			$s.=chr($c);
		}
		else if ($c < 0x800) {
			$s.= chr(0xC0|($c>>6));
			$s.= chr(0x80|($c & 0x3F));
		}
		else if ($c < 0x10000)
		{
			$s.= chr(0xE0|($c>>12));
			$s.= chr(0x80|(($c>>6) & 0x3F));
			$s.= chr(0x80|($c & 0x3F));
		}
		else if ($c < 0x200000)
		{
			$s.= chr(0xE0|($c>>18));
			$s.= chr(0x80|(($c>>12) & 0x3F));
			$s.= chr(0x80|(($c>>6) & 0x3F));
			$s.= chr(0x80|($c & 0x3F));
		}
	}
	return $s;
}

